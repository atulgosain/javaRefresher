package basics;

public class concatenation {

    public static void main(String[] args) {
        String a = "Atul";
        String b = "Gosain";
        System.out.println(a+b);

        int d= 10;
        int e = 15;
        System.out.println(a+b+d+e);
        System.out.println(d+e+a+b);

        System.out.println(a+d+10);

        System.out.println(10+d+a);
    }
}
