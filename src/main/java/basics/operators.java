package basics;

public class operators {


    public static void main(String[] args) {
        int a = 92;
        byte b = (byte) a;
        System.out.println(b);

        int d = 99;
        System.out.println(++d);
int r = 99;
        System.out.println(r++);
        int q = 99;
        System.out.println(++q + ++q);

        System.out.println(q);
    }
}
